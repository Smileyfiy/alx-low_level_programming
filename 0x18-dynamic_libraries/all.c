#include <stdio.h>

/**
 * add - add two intergers
 * @a: first int
 * @b: second int
 * Return: addition
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - substract two intergers
 * @a: first int
 * @b: second int
 * Return: substracted final result
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * dic - divide two intergers
 * @a: first int
 * @b: second int
 * Return: division result
 */
int divide(int a, int b)
{
	return (a / b);
}

/**
 * mod - mod two intergers
 * @a: first int
 * @b: second int
 * Return: final answer
 */
int mod(int a, int b)
{
	return (a % b);
}

/**
 * mul - multiply two intergers
 * @a: first int
 * @b: second int
 * Return: multiply
 */
int mul(int a, int b)
{
	return (a * b);
}
